    private void OpenLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OpenLoginActionPerformed
     
          
         if(UserLogin.getText().equals("")|| PassLogin.getText().equals("")){
        
        SE.GetMessagerErr("Complete Todos Los Campos");
        
        SE.setVisible(true);
        
        return;
        
        }
        
        
        try {
			 //        Polimorfismo: Usa una misma funci칩n para asignar diversos valores a diversos elementos.
			
			
                        PreparedStatement  UserAcess = connect.prepareStatement("Select * From usuarios where UserName = '"+UserLogin.getText()+"'");
						
			PreparedStatement  PassAcess = connect.prepareStatement("Select * From usuarios where Password = '"+PassLogin.getText()+"'");
						
			
			ResultSet DataUser = UserAcess.executeQuery(); 
                        
                        ResultSet DataPass = PassAcess.executeQuery(); 
                        
                         String [] DatosUser = new String[1];
				 
                        String [] DatosPass = new String[1];
                        
                        while(DataUser.next()) {
							
							
							DatosUser[0] = DataUser.getString(2);
							
						}
						
						
						while(DataPass.next()) {
							
							
							DatosPass[0] = DataPass.getString(7);
							
						}
                                          
                                 if(UserLogin.getText().equals(DatosUser[0]) && PassLogin.getText().equals(DatosPass[0])) {
                                 
                                     SectionOptions SO = new SectionOptions();
        
                                    SO.setVisible(true);
        
                                    this.setVisible(false);
                                 }
                                 
                                 else{
                                 
                                 SE.GetMessagerErr("Usuario y/o Contrase침a Incorrectos");
        
                                SE.setVisible(true);
        
                                return;
                                 
                                 }
                                                            
                        
          }catch(SQLException ex){}
        
        
        
        
    }//GEN-LAST:event_OpenLoginActionPerformed

